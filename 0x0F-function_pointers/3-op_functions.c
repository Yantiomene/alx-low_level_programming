#include "3-calc.h"

/**
 * op_add - returns the sum of 2 integers
 * @a: first operand
 * @b: second operand
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the difference of 2 integers
 * @a: first operand
 * @b: second operand
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of 2 integers
 * @a: first operand
 * @b: second operand
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the division of a by b
 * @a: first operand
 * @b: second operand
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of the divison of a by b
 * @a: first operand
 * @b: second operand
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
